from sklearn.metrics import mean_squared_error

def reconstruction_error(real_data, generated_data):
    """
    Calculate the reconstruction error between real and generated data using mean squared error.

    Args:
        real_data: The original data.
        generated_data: The data generated by the model.

    Returns:
        float: The mean squared error between the real and generated data.
    """
    return mean_squared_error(real_data, generated_data)
